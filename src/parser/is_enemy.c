#include "cubed.h"

bool	is_dog(char c)
{
	return (c == 'c' || c == '(' || c == ')' || c == '#' || c == '%');
}

bool	is_guard(char c)
{
	return (c == 'G' || c == '[' || c == ']' || c == '@' || c == '!');
}

bool	is_enemy_tile(char c)
{
	return (c == 'G' || c == 'c' || c == '[' || c == ']' || \
			c == '@' || c == '!' || c == '(' || c == ')' || \
			c == '#' || c == '%');
}
